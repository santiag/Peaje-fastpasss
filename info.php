							<p> El sistema electrónico FAST PASS requiere de dos dispositivos para su

funcionamiento. El primero de ellos es el sensor de lectura infrarrojo, el cual estará

ubicado en la parte superior o techo del puesto de peaje en una posición centrada,

proyectando la luz infrarroja hacia abajo con una inclinación de 45 grados, estos

deberán instalarse en ambos carriles (ida y vuelta) que serán de uso exclusivo

para el tránsito de vehículos que utilicen este sistema para realizar sus pagos,

la posición de los sensores debe contener estas características de instalación

para así facilitar la lectura del segundo de los dispositivos necesarios para el

funcionamiento del sistema, que es una tarjeta que contiene un código de barras,

la cual tiene una medida estándar de 6 cms x 17 cms adherida a la parte interna

de los parabrisas de los vehículos igualmente centrada y en la parte superior.

FAST PASS funciona a través de un sistema pre-pago de saldo electrónico, que

será cargado a la tarjeta que contiene el código de barras para que el sensor

infrarrojo realice la lectura de este código y sistemáticamente descuente de la

base de datos de usuarios, el valor del peaje transitado para poder habilitar

el paso del vehículo por la estación. De lo contrario el paso del vehículo será

restringido y este deberá usar el peaje tradicional. Para poder acceder al servicio

de FAST PASS, el usuario debe acercarse a los centros de pago que serán de

fácil acceso, pues se encontraran ubicados estratégicamente en las terminales

de transporte de la región y en las principales estaciones de servicio localizadas

a lo largo de la malla vial de la autopista del café. Al momento de acercarse a

efectuar el pago, el personal del punto de pago carga la tarjeta con el número de

peajes que el usuario haya pagado para así garantizar el libre flujo vehicular por la

autopista. </p>
							
